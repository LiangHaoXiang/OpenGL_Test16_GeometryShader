    if (!gladLoadGLLoader((GLADloadproc)glfwGetProcAddress))
    {
        std::cout << "Failed to initialize GLAD" << std::endl;
        return -1;
    }

    // configure global opengl state
    // -----------------------------
    glEnable(GL_DEPTH_TEST);
    // build and compile shaders
    // -------------------------
    char* headDir = "/Users/haoxiangliang/Desktop/代码草稿/OpenGL/OpenGL_Test16_GeometryShader/OpenGL_Test16_GeometryShader/";
    string p1 = string(headDir) + "CubeShader/CubeVertexShader.cpp";
    string p2 = string(headDir) + "CubeShader/CubeFragmentShader.cpp";
    string p8 = string(headDir) + "CubeShader/CubeGeometryShader.cpp";
    Shader cubeShader(p1.c_str(), p2.c_str(), p8.c_str());
    string p3 = string(headDir) + "SkyShader/SkyVertexShader.cpp";
    string p4 = string(headDir) + "SkyShader/SkyFragmentShader.cpp";
    Shader skyboxShader(p3.c_str(), p4.c_str());
    string p5 = string(headDir) + "ModelShader/ModelVertexShader.cpp";
    string p6 = string(headDir) + "ModelShader/ModelFragmentShader.cpp";
    string p7 = string(headDir) + "ModelShader/ModelGeometryShader.cpp";
    Shader modelShader(p5.c_str(), p6.c_str(), p7.c_str());
    
    // cube VAO
    unsigned int cubeVAO, cubeVBO;
    glGenVertexArrays(1, &cubeVAO);
    glGenBuffers(1, &cubeVBO);
    glBindVertexArray(cubeVAO);
    glBindBuffer(GL_ARRAY_BUFFER, cubeVBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(NULL), NULL, GL_STATIC_DRAW);
    void *ptr = glMapBuffer(GL_ARRAY_BUFFER, GL_WRITE_ONLY);
    memcpy(ptr, cubeVertices, sizeof(cubeVertices));
    glUnmapBuffer(GL_ARRAY_BUFFER);
    
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(1);
    glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)(3 * sizeof(float)));
    // load skybox
    // -----------
    // skybox VAO
    unsigned int skyboxVAO, skyboxVBO;
    glGenVertexArrays(1, &skyboxVAO);
    glGenBuffers(1, &skyboxVBO);
    glBindVertexArray(skyboxVAO);
    glBindBuffer(GL_ARRAY_BUFFER, skyboxVBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(skyboxVertices), &skyboxVertices, GL_STATIC_DRAW);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);
    
    // load textures
    // -------------
    vector<string> faces
    {
        "/Users/haoxiangliang/Desktop/未命名文件夹/skybox/right.jpg",
        "/Users/haoxiangliang/Desktop/未命名文件夹/skybox/left.jpg",
        "/Users/haoxiangliang/Desktop/未命名文件夹/skybox/top.jpg",
        "/Users/haoxiangliang/Desktop/未命名文件夹/skybox/bottom.jpg",
        "/Users/haoxiangliang/Desktop/未命名文件夹/skybox/front.jpg",
        "/Users/haoxiangliang/Desktop/未命名文件夹/skybox/back.jpg"
    };
    unsigned int cubemapTexture = loadCubemap(faces);

    // load models
    // -----------
    Model ourModel("/Users/haoxiangliang/Desktop/未命名文件夹/nanosuit_reflection/nanosuit.obj");
    
    unsigned int cubeTexture = loadTexture("/Users/haoxiangliang/Desktop/未命名文件夹/container.jpg");
    
    //Uniform缓冲
    //----projection
    unsigned int uniformBlockIndexCube_projection = glGetUniformBlockIndex(cubeShader.ID, "Matrices_Projection");
    unsigned int uniformBlockIndexModel_projection = glGetUniformBlockIndex(modelShader.ID, "Matrices_Projection");
    unsigned int uniformBlockIndexSky_projection = glGetUniformBlockIndex(skyboxShader.ID, "Matrices_Projection");
    int bindPoint0 = 0;//绑定点0
    glUniformBlockBinding(cubeShader.ID, uniformBlockIndexCube_projection, bindPoint0);
    glUniformBlockBinding(modelShader.ID, uniformBlockIndexModel_projection, bindPoint0);
    glUniformBlockBinding(skyboxShader.ID, uniformBlockIndexSky_projection, bindPoint0);
    
    unsigned int uboMatrices_projection;
    glGenBuffers(1, &uboMatrices_projection);
    glBindBuffer(GL_UNIFORM_BUFFER, uboMatrices_projection);
    glBufferData(GL_UNIFORM_BUFFER, 1 * sizeof(mat4), NULL, GL_STATIC_DRAW);
    glBindBuffer(GL_UNIFORM_BUFFER, 0);
    glBindBufferRange(GL_UNIFORM_BUFFER, bindPoint0, uboMatrices_projection, 0, 1 * sizeof(mat4));
    //----view
    unsigned int uniformBlockIndexCube_view = glGetUniformBlockIndex(cubeShader.ID, "Matrices_View");
    unsigned int uniformBlockIndexModel_view = glGetUniformBlockIndex(modelShader.ID, "Matrices_View");
    int bindPoint1 = 1;//绑定点1
    glUniformBlockBinding(cubeShader.ID, uniformBlockIndexCube_view, bindPoint1);
    glUniformBlockBinding(modelShader.ID, uniformBlockIndexModel_view, bindPoint1);
    
